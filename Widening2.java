class Widening2 
{
	public static void main(String[] args) 
	{
      byte b1 = 100;
	  short s1 = b1;
	  System.out.println(s1);
	  
	  int i1 = b1;
	  System.out.println(i1);
	  
	  long l1 = b1;
	  System.out.println(l1);
	  
	  float f1 = b1;
	  System.out.println(f1);
	  
	  double d1 = b1;
	  System.out.println(d1);
	}
}
