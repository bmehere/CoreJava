class Widening
{
	public static void main(String[] args) 
	{
	  double d1 =100;
	  System.out.println(d1);
	  
	  int i = 'a';
	  System.out.println(i);
	  
	  double d2 = 'z';
	  System.out.println(d2);
	  
	  float f1 = 1200l;
	  System.out.println(f1);
	  
	  double d3 = 123.123f;
	  System.out.println(d3);
	  
	  
	}
}
